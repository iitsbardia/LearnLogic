# Define Variables
num1 = 10
num2 = 5

# Addition
result = num1 + num2
print("num1 + num2 =", result)

# Subtraction
result = num1 - num2
print("num1 - num2 =", result)

# Multiplication
result = num1 * num2
print("num1 * num2 =", result)

# Division
result = num1 / num2
print("num1 / num2 =", result)

# Power
result = num1 ** 2
print("num1 ** 2 =", result)

# Modulus
result = 10 % 3
print("10 % 3 =", result)

