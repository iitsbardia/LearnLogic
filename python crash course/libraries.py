import math

# Calculate the square root of 16
sqrt_result = math.sqrt(16)
print("Square root of 16:", sqrt_result)

# Calculate the factorial of 5
factorial_result = math.factorial(5)
print("Factorial of 5:", factorial_result)

# Use mathematical constants pi and e
circle_area = math.pi * (5 ** 2)  # Area of a circle with radius 5
print("Area of a circle with radius 5:", circle_area)
