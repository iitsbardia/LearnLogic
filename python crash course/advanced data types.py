# lists

my_list = [1, 2, True, "hello", [10, 20]]

# tuples

my_tuple = (10, 20, 30)
print(my_tuple[0]) # Output: 10

# dicts

person = {'name': 'Alice', 'age': 25}
print(person)

print(person['name'])  # Output: Alice
print(person['age'])   # Output: 25

person['city'] = 'Toronto'
print(person)

# sets

my_set = {1, 2, 3, 3, 4}
print(my_set)  # Output: {1, 2, 3, 4}
